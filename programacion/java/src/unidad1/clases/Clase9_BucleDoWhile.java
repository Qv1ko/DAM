package unidad1.clases;

public class Clase9_BucleDoWhile {
    
    public static void main(String[] args) {

        //ejemplo 0 - Numeros pares de 100 a 0
        
        int num=100;

        do {

            System.out.println(num);
            num-=2;
            
        }
        while(num>=0);

    }

}
