package unidad7.clase;

public class Animal {

    private String especie;

    public Animal() {
        this.especie="";
    }

    public Animal(String especie) {
        this.especie=especie;
    }

    public String getEspecie() {
        return especie;
    }

    public void setEspecie(String especie) {
        this.especie = especie;
    }

}
